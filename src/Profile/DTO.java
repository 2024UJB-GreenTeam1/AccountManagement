package Profile;

public class DTO {
	private String name;
	private String ID;
	private String email;
	private String phone;
	private String year;
	private char height;
	private char weight;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public char getHeight() {
		return height;
	}
	public void setHeight(char height) {
		this.height = height;
	}
	public char getWeight() {
		return weight;
	}
	public void setWeight(char weight) {
		this.weight = weight;
	}
	
}
